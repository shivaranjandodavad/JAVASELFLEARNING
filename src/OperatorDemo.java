public class OperatorDemo {
    public static void main(String args[]){
        int num1=10,num2=2;
        int sum=num1+num2;
        int diff=num1-num2;
        int mul=num1*num2;
        int div=num1/num2;
        System.out.println("sum ="+sum);
        System.out.println("diff ="+diff);
        System.out.println("mul ="+mul);
        System.out.println("division ="+div);
    }
}
